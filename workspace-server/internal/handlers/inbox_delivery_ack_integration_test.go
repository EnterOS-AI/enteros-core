// inbox_delivery_ack_integration_test.go — REAL Postgres integration tests
// for MUST-FIX 3 (durable acked delivery + conservative prune) and the
// MUST-FIX 4 durable fix (FAILED branch emits a delegate_result row).
//
// Run with:
//
//	docker run --rm -d --name pg-integration \
//	  -e POSTGRES_PASSWORD=test -e POSTGRES_DB=molecule \
//	  -p 55432:5432 postgres:15-alpine
//	sleep 4
//	# apply migrations (boot runner or psql loop), then:
//	cd workspace-server
//	INTEGRATION_DB_URL="postgres://postgres:test@localhost:55432/molecule?sslmode=disable" \
//	  go test -tags=integration ./internal/handlers/ -run Integration_InboxAck -run Integration_Prune -run Integration_UpdateStatusFailed
//
// Why these are NOT plain sqlmock unit tests: sqlmock can pin which SQL
// fires but cannot execute the predicate, so it cannot prove that the prune
// actually RETAINS an old-but-unacked row, or that GREATEST actually keeps
// the higher cursor across two POSTs. These boot a real Postgres and SELECT
// the resulting row state.

